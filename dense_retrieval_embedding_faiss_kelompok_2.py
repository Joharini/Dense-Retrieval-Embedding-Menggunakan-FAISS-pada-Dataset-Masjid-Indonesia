# MEMBANGUN DAN MENYIMPAN FAISS INDEX
import os
save_folder = "/content/drive/MyDrive/Kelompok_2_UAS_AIR/dense_index"

os.makedirs(save_folder, exist_ok=True)
for model_name, embeddings in embeddings_dict.items():

    print("="*60)
    print(f"MEMBANGUN FAISS INDEX : {model_name}")
    print("="*60)

    emb_copy  = embeddings.copy().astype(np.float32)
    faiss.normalize_L2(emb_copy)

    dimension = emb_copy.shape[1]
    index     = faiss.IndexFlatIP(dimension)
    index.add(emb_copy)

    indexes[model_name] = index

    index_path = os.path.join(save_folder, model_name.replace("-", "_") + ".index")
    faiss.write_index(index, index_path)

    print(f"Jumlah Vektor : {index.ntotal:,}")
    print(f"Dimensi       : {dimension}")
    print(f"Disimpan ke   : {index_path}\n")

"""---
# **Bagian 3 – Dense Search**

## 3.1 Fungsi Dense Search
"""

def dense_search(model_name, query, top_k=10):
    # Memanggil model embedding
    model = loaded_models[model_name]
    # Mengubah query menjadi embedding
    query_embedding = model.encode([query], convert_to_numpy=True).astype(np.float32)
    # Normalisasi Cosine Similarity
    faiss.normalize_L2(query_embedding)
    # Pencarian menggunakan FAISS
    similarity, indices = indexes[model_name].search(query_embedding, top_k)

    results = corpus.iloc[indices[0]][["name","address", "provinsi", "kabko", "kecamatan","rating", "mosque_type"]].copy()
    # Menambahkan Document iD
    results.insert(0, "document_ID", corpus.iloc[indices[0]].index)
    # Menambahkan informasi hasil retrieval
    results["similarity"] = similarity[0]
    results["rank"]       = range(1, len(results) + 1)
    results["query"]      = query
    results["model"]      = model_name

    # Mengatur urutan kolom
    results = results[
        [
            "rank",
            "document_ID",
            "query",
            "model",
            "name",
            "address",
            "provinsi",
            "kabko",
            "kecamatan",
            "mosque_type",
            "rating",
            "similarity"
        ]
   ]

    return results.reset_index(drop=True)

print("Fungsi dense_search siap digunakan")

