"""**Cell 21 - Membuat Fungsi Dense Semantic Search**"""

# ======================================================
# CELL 21 : FUNGSI DENSE SEMANTIC SEARCH MENGGUNAKAN FAISS INDEX
# ======================================================

def dense_search(model_name, query, top_k=10):

    # Mengambil model
    model = loaded_models[model_name]

    # Mengubah query menjadi embedding
    query_embedding = model.encode(
        [query],
        convert_to_numpy=True
    )

    # Normalisasi Cosine Similarity
    faiss.normalize_L2(query_embedding)

    # Pencarian pada FAISS
    similarity, indices = indexes[model_name].search(
        query_embedding,
        top_k
    )

    # Mengambil hasil
    results = corpus.iloc[indices[0]].copy()

    # Similarity
    results["Similarity"] = similarity[0]

    # Membersihkan kolom yang ditampilkan
    for col in ["name", "address", "provinsi", "kabko", "kecamatan"]:
        results[col] = (
            results[col]
            .fillna("alamat tidak tersedia" if col == "address" else "")
            .apply(clean_text)
        )

    return results[
        [
            "name",
            "address",
            "provinsi",
            "kabko",
            "kecamatan",
            "Similarity"
        ]
    ]

